//解码函数的LT矩阵
static void raptor_getLT_d(RParamDec_t *para)
{
    uint32 i, j, temp;
    uint8 **mat = para->Amat->rowpoint;
    Triple_t *trp = (Triple_t *)malloc(sizeof(Triple_t));
    int l1 = L1[para->K];
    for(i=0; i<para->N; i++){
        trip(para->K, para->list[i], trp);
        while(trp->b >= para->L)
            trp->b = (trp->b + trp->a) % l1;
        mat[i+para->S+para->H][trp->b] = 1;
        temp = (uint32)min(trp->d-1,para->L-1);
        for(j=1; j<=temp; j++){
            trp->b = (trp->b+trp->a)%l1;
            while (trp->b >= para->L)
                trp->b = (trp->b+trp->a)%l1;
            mat[i+para->S+para->H][trp->b] = 1;
        }
    }
    free(trp);
    trp = NULL;
}

