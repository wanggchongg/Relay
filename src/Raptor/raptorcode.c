//解码函数
int raptor_decode(RParamDec_t *para, uint8 *input, uint8 *output_return, uint32 T)
{
    raptor_intermediate_d(para);//计算矩阵的值
    if(!matrix_equations(para->Amat, input, T))//高斯消元法 通过将原矩阵化为单位矩阵的过程将输入的symbols恢复成intermediate symbols
        return 0;
    uint32 temp;
    uint8 **mat = para->Amat->rowpoint;
    uint8 *output;
    output = (uint8 *)malloc(para->L*T);
    memset(output, 0, para->L*T);

    Triple_t *trp = (Triple_t *)malloc(sizeof(Triple_t));
    int l1 = L1[para->K];
    uint32 i, j;
    for(i = 0; i<para->K; i++){//求出intermediate symbols与填充零后的source symbols相关联的LT矩阵 也就是编码时声称的A矩阵
        memset(mat[i+para->S+para->H], 0, para->L);

        trip(para->K,i,trp);
        while(trp->b>=para->L)
            trp->b = (trp->b+trp->a)%l1;
        mat[i+para->S+para->H][trp->b] = 1;
        temp = (uint32)min(trp->d-1,para->L-1);
        for(uint32 j = 1;j<=temp;j++){
            trp->b = (trp->b+trp->a)%l1;
            while (trp->b>=para->L)
                trp->b = (trp->b+trp->a)%l1;
            mat[i+para->S+para->H][trp->b] = 1;
        }
     }
    free(trp);
    trp = NULL;

    for(j=0; j<para->L; j++){//根据A矩阵恢复source symbols
        for(uint32 i=0; i<para->L; i++){
            if(para->Amat->rowpoint[j][i] == 1)
                my_xor(output+j*T , input+i*T, T);
        }
    }

    memcpy(output_return, output+(para->H+para->S)*T, para->K*T);
    free(output);
    return 1;
}

