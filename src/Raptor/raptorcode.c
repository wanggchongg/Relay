//伪随机数生成器
static uint32 Rand(uint32 x,uint32 i,uint32 m)
{
	uint32 temp;
	//	printf("m:%d\n",m);
	temp = (V0[(x+i)%256]^V1[((x/256)+i)%256])%m;
	return temp;
}

