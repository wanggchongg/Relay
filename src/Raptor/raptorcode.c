//初始化解码器参数
static void *raptor_init_d(uint32 k, uint32 n, void *arg)
{
	RParamDec_t *para = (RParamDec_t *)arg;
    para->K = k;
    para->S = S[k];
    para->H = H[k];
    para->L = para->K + para->S + para->H;
    para->N = n;	//接收到的分片数
    para->Amat = (Matrix_t *)malloc(sizeof(Matrix_t));
    matrix_init(para->S + para->H + n, para->L, para->Amat);//初始化所需矩阵
    para->list = (uint16*)malloc(n * sizeof(uint16));//分片的esi列表
    return para;
}

